package com.example.appfinalmovile.RetroFit.Response;

public class ResponseLocales {
    private Boolean rptL;
    private String mensajeL;
    private Integer codigo;
    private String nombre;
    private String descripcion;
    private String aforo;
    private String telefono;
    private String ubicacion;
    private String imagen;

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public Boolean getRptL() {
        return rptL;
    }

    public void setRptL(Boolean rptL) {
        this.rptL = rptL;
    }

    public String getMensajeL() {
        return mensajeL;
    }

    public void setMensajeL(String mensajeL) {
        this.mensajeL = mensajeL;
    }

    public Integer getCodigo() {
        return codigo;
    }

    public void setCodigo(Integer codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getAforo() {
        return aforo;
    }

    public void setAforo(String aforo) {
        this.aforo = aforo;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getUbicacion() {
        return ubicacion;
    }

    public void setUbicacion(String ubicacion) {
        this.ubicacion = ubicacion;
    }

    public String getImagen() {
        return imagen;
    }
}
